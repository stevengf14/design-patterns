package ec.com.learning.behavioral.model;

/**
 *
 * @author Steven - September 2022
 */
public class Person {

    private int id;
    private String names;
    private int age;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNames() {
        return names;
    }

    public void setNames(String names) {
        this.names = names;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
